    EXTRACT(YEAR FROM data_vencimento)::int as ano,
    EXTRACT(MONTH FROM data_vencimento)::int as mes,
    COUNT(*) as quantidade,
    SUM(valor_previsto) as total_previsto
FROM movimentacoes_financeiras
WHERE tipo = 'despesa'
GROUP BY ano, mes
ORDER BY ano, mes;
